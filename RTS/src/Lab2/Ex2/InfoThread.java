package Lab2.Ex2;

public class InfoThread {
    int id;
    int c;

    public InfoThread(int id, int c) {
        this.id = id;
        this.c = c;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }
}
